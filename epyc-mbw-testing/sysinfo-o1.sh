#!/usr/bin/env bash

echo "============================================================"
echo "                   System Info Collection"
echo "============================================================"
echo

################################################################################
# 1. lstopo – Show hardware topology
################################################################################
echo "=== lstopo ================================================="
lstopo
echo

################################################################################
# 2. dmidecode – Show CPU/cache/memory info from SMBIOS
################################################################################
echo "=== dmidecode -t processor -t cache -t memory =============="
sudo dmidecode -t processor -t cache -t memory
echo

################################################################################
# 3. inxi – Summarize CPU info
################################################################################
echo "=== inxi -C ================================================"
inxi -C
echo

################################################################################
# 4. numactl – Show NUMA layout
################################################################################
echo "=== numactl --hardware ====================================="
numactl --hardware
echo

################################################################################
# 5. /proc/cpuinfo – Low-level CPU info
################################################################################
echo "=== cat /proc/cpuinfo ======================================"
cat /proc/cpuinfo
echo

################################################################################
# 6. hwinfo – CPU, BIOS, memory details
################################################################################
echo "=== hwinfo --cpu --bios --memory ==========================="
sudo hwinfo --cpu --bios --memory
echo

################################################################################
# 7. lscpu – CPU architecture overview
################################################################################
echo "=== lscpu =================================================="
lscpu
echo

################################################################################
# 8. lscpu -C – Detailed cache info
################################################################################
echo "=== lscpu -C ==============================================="
lscpu -C
echo

echo "============================================================"
echo "    All system-info commands have finished running."
echo "============================================================"
